
// switch

let color = 'redish';

switch (color) {
  case 'red':
    console.log('color is red');
    break;
  case 'blue':
      console.log('color is blue');
      break;
  case 'green':
        console.log('color is green');
        break;
  case 'black':
     console.log('color is black');
     break;
  case 'blackish','blueish','redish' :
        console.log('color is black or blueish or redish');
        break;
  default:
      console.log('no color');
    break;
};




